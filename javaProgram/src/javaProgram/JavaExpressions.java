package javaProgram;

public class JavaExpressions {

	public static void main(String[] args) {
		int addNumber = 12 + 11; // addition
		int minusNumber = 23 - 14; // subtraction
		int multiplyNumber = 4 * 7; // multiplication
		double divideNumber = 34 / 7; // division
		int modulus = 20 & 2; // modulus
		
		System.out.println("Addition result: " + addNumber);
		System.out.println("Subtraction result: " + minusNumber);
		System.out.println("Multiplication result: " + multiplyNumber);
		System.out.println("division result: " + divideNumber);
		System.out.println("Remainder result: " + modulus);
	}

}
