package com.firstprogram.logical;

public class LogicalOperators {
    public static void main(String[] args) {
        // LOGICAL OPERATORS
        Boolean varabialeX = true;
        Boolean varabialeY = false;

        System.out.println(varabialeX && varabialeY); // logical AND
        System.out.println(varabialeX || varabialeY); // logical OR
        System.out.println(!varabialeY); // logical NOT
    }
}
