package com.firstprogram.incrementdecrement;

public class IncrementDecrement {
    public static void main(String[] args) {
        // INCREMENT & DECREMENT
        int value = 0;

        value++;
        System.out.println(value);

        value--;
        System.out.println(value);

        // assignment operators
        value += 1;
        System.out.println(value);

        value -= 1;
        System.out.println(value);
    }
}
